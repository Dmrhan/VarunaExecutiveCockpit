/**
 * Normalizes a currency code (numeric or string) to a standard ISO string.
 * @param code The currency code to normalize
 * @returns The ISO string (e.g., 'TRY') or the original code if no mapping found.
 */
export const normalizeCurrency = (code: string | number | null | undefined): string => {
    if (code === null || code === undefined) return 'TRY';

    const numericCode = typeof code === 'string' ? parseInt(code, 10) : code;

    if (!isNaN(numericCode) && currencyMap[numericCode]) {
        return currencyMap[numericCode];
    }

    return String(code).toUpperCase();
};
